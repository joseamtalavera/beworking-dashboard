/**
 * Get the display label for a country in the given locale.
 */
export const getCountryLabel = (country, lang = 'es') =>
  typeof country === 'string' ? country : (country[lang] || country.es);

/**
 * Check if a stored country value represents Spain (works for both locales).
 */
export const isSpain = (value) =>
  value === 'España' || value === 'Spain';

/**
 * Filter countries by search term — searches in both ES and EN names.
 */
export const filterCountries = (options, inputValue) => {
  if (!inputValue) return options;
  const search = inputValue.toLowerCase();
  return options.filter(
    (opt) =>
      opt.es.toLowerCase().includes(search) ||
      opt.en.toLowerCase().includes(search)
  );
};
